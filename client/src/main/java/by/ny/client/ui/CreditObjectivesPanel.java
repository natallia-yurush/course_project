    private void clearCreditFields() {
        creditObjectiveField.setText("");
        
        try {
            ObjectivesTable.clearSelection();
        } catch (Exception e) {
            
        }
        deleteButton.setEnabled(false);
    }
    
    private void readObjectivesToFile() {
        try {
            PrintWriter writer = new PrintWriter("output.txt", "windows-1251");
            for(String str : creditsObjectives) {
                writer.write(str + System.getProperty("line.separator"));
                
            }
            writer.close();
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    private void readObjectivesFromFile() {
        try (BufferedReader reader = new BufferedReader(new InputStreamReader( new FileInputStream("output.txt") , "windows-1251"))) {
            String line;
	    while ((line = reader.readLine()) != null) {
                creditsObjectives.add(line);
	    }
        }
            catch (Exception e) {
                e.printStackTrace();
            }
    }
    
    private void reloadTable() {
        int rowsCount = ((DefaultTableModel) ObjectivesTable.getModel()).getRowCount();
            
            for (int i = 0; i < rowsCount; i ++) {
                ((DefaultTableModel) ObjectivesTable.getModel()).removeRow(0);
            }
            
            for (int i = 0; i < creditsObjectives.size(); i ++) {
                Object[] row = new Object[2];
                
                row[0] = i+1;
                row[1] = creditsObjectives.get(i);
                
                ((DefaultTableModel) ObjectivesTable.getModel()).addRow(row);
            }
    }

