    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(creditsObjectives.size() == 4) {
            clearCreditFields();
            JOptionPane.showMessageDialog(this, "Невозможно добавить больше 4 целей");
            return;
        }
        
        creditsObjectives.add((String)creditObjectiveField.getText());
        readObjectivesToFile();
        
        clearCreditFields();
        reloadTable();        
    }//GEN-LAST:event_saveButtonActionPerformed

