    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        if(field01.getText().equals("") || field02.getText().equals("") || field03.getText().equals("") ||
                field10.getText().equals("") || field12.getText().equals("") || field13.getText().equals("") || 
                field20.getText().equals("") || field21.getText().equals("") || field23.getText().equals("") || field30.getText().equals("")
                 || field31.getText().equals("") || field32.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Должны быть заполнены все ячейки матрицы");
            return;
        }
        double temp1 = 0.0, temp2 = 0.0, temp3 = 0.0, temp4 = 0.0;
        
        temp1 = (Double.parseDouble(field01.getText()) + Double.parseDouble(field02.getText()) 
                + Double.parseDouble(field03.getText())) / Integer.parseInt(ScaleSizeField.getText()) / 12;
        
        temp2 = (Double.parseDouble(field10.getText()) + Double.parseDouble(field12.getText()) 
                + Double.parseDouble(field13.getText())) / Integer.parseInt(ScaleSizeField.getText()) / 12;
        
        temp3 = (Double.parseDouble(field20.getText()) + Double.parseDouble(field21.getText()) 
                + Double.parseDouble(field23.getText())) / Integer.parseInt(ScaleSizeField.getText()) / 12;
        
        temp4 = (Double.parseDouble(field30.getText()) + Double.parseDouble(field31.getText()) 
                + Double.parseDouble(field32.getText())) / Integer.parseInt(ScaleSizeField.getText()) / 12;
        
        targetWeights[0] += temp1;
        targetWeights[1] += temp2;
        targetWeights[2] += temp3;
        targetWeights[3] += temp4;
        
        JOptionPane.showMessageDialog(this, "Матрица частот эксперта успешно сформирована");
        
        //clearFields();
    }//GEN-LAST:event_SaveButtonActionPerformed

    private void CalculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateButtonActionPerformed
        if(targetWeights[0] == 0.0 && targetWeights[1] == 0.0 && targetWeights[2] == 0.0){
            JOptionPane.showMessageDialog(this, "Данные не были введены.");
            return;
        }
        
        String str = "Искомые веса целей:" + System.getProperty("line.separator") + "К1 = " + targetWeights[0].toString()
                + System.getProperty("line.separator") + "К2 = " + targetWeights[1].toString()
                + System.getProperty("line.separator") + "К3 = " + targetWeights[2].toString()
                + System.getProperty("line.separator") + "К4 = " + targetWeights[3].toString()
                + System.getProperty("line.separator") + "Наиболее весомая цель: " + creditsObjectives.get(max());
        
        ResponseField.setText(str);
        clearFields();
    }//GEN-LAST:event_CalculateButtonActionPerformed

