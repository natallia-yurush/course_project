    private void readObjectivesFromFile() {
        try (BufferedReader reader = new BufferedReader(new InputStreamReader( new FileInputStream("output.txt") , "windows-1251"))) {
            String line;
	    while ((line = reader.readLine()) != null) {
                creditsObjectives.add(line);
	    }
        }
            catch (Exception e) {
                e.printStackTrace();
            }
    }
    
    private void fillCriteriaField() {
        //System.getProperty("line.separator");
        CriteriaField.setText("Критерии:" + System.getProperty("line.separator") + "К1: " + creditsObjectives.get(0) + System.getProperty("line.separator") 
                + "К2: " + creditsObjectives.get(1) + System.getProperty("line.separator") + "К3: " + creditsObjectives.get(2)
                + System.getProperty("line.separator") + "К4: " + creditsObjectives.get(3));
    }
    
    private Integer max() {
       Integer maxIndex = 0; 
       Double currentNumber;
       for (int i = 1; i < targetWeights.length; i++) {
            currentNumber = targetWeights[i];
            if ((currentNumber > targetWeights[maxIndex])){
                maxIndex = i;
            }
        } 
        return maxIndex; 
    } 

